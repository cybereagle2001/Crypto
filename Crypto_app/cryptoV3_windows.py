# base64 encryption_module
def base64_enc():
    os.system('cls')
    print('\033[0;32m')
    banner=pyfiglet.figlet_format('BASE 64')
    print(banner)
    print("\033[0m")
    original_text=input("write your text to encrypt: ")
    message_bytes= original_text.encode('ascii')
    encrypted_text=base64.b64encode(message_bytes)
    base64_message= encrypted_text.decode('ascii')
    file= open("encode.txt","w")
    file.write("your encoded text is :" + "\n" + base64_message + "\n")
    file.close()
    print("\033[1;36m you will find the encoded text in encode.txt")

# crypto1
def crypto1_enc():
    os.system('cls')
    print('\033[0;32m')
    banner=pyfiglet.figlet_format('crypto 1')
    print(banner)
    print("\033[0m")
    message= input("write the text to encrypt: ")
    message_bytes= message.encode('ascii')
    base64_bytes= base64.b64encode(message_bytes)
    base64_message= base64_bytes.decode('ascii') 
    str_base64= repr(base64_message)
    encryption= codecs.getencoder("ROT-13")
    text_rot13=encryption(str_base64)[0]
    longs= len(text_rot13)
    text= text_rot13[1:longs-1]
    file = open("encode.txt","w")
    file.write("your encoded text is :"+"\n"+ text + "\n")
    file.close()
    print("\033[1;36m you will find the encoded text in encode.txt")

